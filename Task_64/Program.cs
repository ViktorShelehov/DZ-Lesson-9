Console.Clear();

int n = GetNumberFromUser("Введите число N: ", "Ошибка ввода! "); 

Console.WriteLine("Натуральные числа от N до 1:");
PrintNaturalNums(n);

///////////////////////////////////////////////////////////////////////////////
///////////////////////- Метод ввода данных -/////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

int GetNumberFromUser(string message, string ErrorMessage)
{
    while(true)
    {
        Console.Write(message);
        if(int.TryParse(Console.ReadLine(), out int userNumber))
        return userNumber;
        Console.Write(ErrorMessage);
    }
}
////////////////////////////////////////////////////////////////////////////////
////////////////////////- Решение задачи -/////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////
void PrintNaturalNums(int n)
{
    if (n >= 1)
    {
        Console.WriteLine(n);
        PrintNaturalNums(n - 1);
    }
}
