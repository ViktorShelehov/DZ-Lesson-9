//Задайте значение M и N. Напишите программу, которая найдёт сумму
//натуральных элементов в промежутке от M до N
//M = 1; N = 15 -> 120
Console.Clear();

int m = GetNumberFromUser("Введите первое число М: ", "Ошибка ввода! ");
int n = GetNumberFromUser("Введите второе число N: ", "Ошибка ввода! ");

int sum = CalculateSum(m, n);

Console.WriteLine("Сумма натуральных элементов от M до N: " + sum);


///////////////////////////////////////////////////////////////////////////////
///////////////////////- Метод ввода данных -/////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

int GetNumberFromUser(string message, string ErrorMessage)
{
    while(true)
    {
        Console.Write(message);
        if(int.TryParse(Console.ReadLine(), out int userNumber))
        return userNumber;
        Console.Write(ErrorMessage);
    }
}
////////////////////////////////////////////////////////////////////////////////
////////////////////////- Решение задачи -/////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////
int CalculateSum(int m, int n)
{
    if (m == n)
    {
        return m;
    }
    else return m + CalculateSum(m + 1, n);  
}
